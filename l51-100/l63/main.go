func uniquePathsWithObstacles(obstacleGrid [][]int) int {
    if len(obstacleGrid) < 2 {
        for _, v := range obstacleGrid[0] {
            if v == 1 {
                return 0
            }
        }
        return 1
    }
    if len(obstacleGrid[0]) < 2 {
        for _, v := range obstacleGrid {
            if v[0] == 1 {
                return 0
            }
        }
        return 1
    }

    data := make([][]int, len(obstacleGrid))
    for i, v := range obstacleGrid {
        items := make([]int, len(v))
        for j := range items {
            items[j] = -1
        }
        data[i] = items
    }
    return walkPathsWithObstacles(obstacleGrid, data, 0, 0)
}

func walkPathsWithObstacles(obstacleGrid, data [][]int, row, column int) int {
    if obstacleGrid[row][column] == 1 {
        data[row][column] = 0
        return 0
    }
    if row == len(obstacleGrid)-1 && column == len(obstacleGrid[row])-1 {
        data[row][column] = 1
        return 1
    }
    if data[row][column] != -1 {
        return data[row][column]
    }
    var res int
    if row < len(obstacleGrid)-1 {
        res += walkPathsWithObstacles(obstacleGrid, data, row+1, column)
    }
    if column < len(obstacleGrid[row])-1 {
        res += walkPathsWithObstacles(obstacleGrid, data, row, column+1)
    }
    data[row][column] = res
    return res
}
